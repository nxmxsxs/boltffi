import SwiftUI
import BenchBoltFFI
import BenchUniffi

struct BenchmarkResult: Identifiable {
    let id = UUID()
    let name: String
    let category: String
    let boltffiTimeNs: UInt64
    let uniffiTimeNs: UInt64
    var speedup: Double { Double(uniffiTimeNs) / Double(max(boltffiTimeNs, 1)) }
}

struct ContentView: View {
    @State private var results: [BenchmarkResult] = []
    @State private var isRunning = false
    @State private var currentTest = ""
    @State private var progress: Double = 0
    
    var body: some View {
        NavigationView {
            VStack(spacing: 0) {
                if isRunning {
                    VStack {
                        ProgressView(value: progress)
                            .padding()
                        Text(currentTest)
                            .font(.caption)
                            .foregroundColor(.secondary)
                    }
                    .padding()
                }
                
                if results.isEmpty && !isRunning {
                    Spacer()
                    Text("Tap 'Run All Benchmarks' to start")
                        .foregroundColor(.secondary)
                    Spacer()
                } else {
                    List {
                        ForEach(groupedResults.keys.sorted(), id: \.self) { category in
                            Section(header: Text(category)) {
                                ForEach(groupedResults[category] ?? []) { result in
                                    ResultRow(result: result)
                                }
                            }
                        }
                    }
                }
                
                HStack(spacing: 12) {
                    Button(action: runBenchmarks) {
                        Label(isRunning ? "Running..." : "Run All", systemImage: "play.fill")
                            .frame(maxWidth: .infinity)
                    }
                    .buttonStyle(.borderedProminent)
                    .disabled(isRunning)
                    
                    if !results.isEmpty {
                        ShareLink(item: generateReport()) {
                            Label("Share", systemImage: "square.and.arrow.up")
                        }
                        .buttonStyle(.bordered)
                    }
                }
                .padding()
            }
            .navigationTitle("BoltFFI vs UniFFI")
            .navigationBarTitleDisplayMode(.inline)
        }
    }
    
    var groupedResults: [String: [BenchmarkResult]] {
        Dictionary(grouping: results, by: { $0.category })
    }
    
    func generateReport() -> String {
        let device = UIDevice.current
        var report = """
        # BoltFFI vs UniFFI Benchmark Results
        
        **Device:** \(device.model) (\(device.systemName) \(device.systemVersion))
        **Date:** \(Date().formatted())
        
        | Benchmark | BoltFFI | UniFFI | Speedup |
        |-----------|------|--------|---------|
        """
        
        for result in results {
            let boltffiStr = formatTime(result.boltffiTimeNs)
            let uniffiStr = formatTime(result.uniffiTimeNs)
            let speedupStr = String(format: "%.1fx", result.speedup)
            report += "\n| \(result.name) | \(boltffiStr) | \(uniffiStr) | \(speedupStr) |"
        }
        
        report += "\n\n---\nGenerated by BoltFFI iOS Benchmark"
        return report
    }
    
    func formatTime(_ ns: UInt64) -> String {
        if ns >= 1_000_000 {
            return String(format: "%.2f ms", Double(ns) / 1_000_000)
        } else if ns >= 1_000 {
            return String(format: "%.2f μs", Double(ns) / 1_000)
        } else {
            return "\(ns) ns"
        }
    }
    
    func runBenchmarks() {
        isRunning = true
        results = []
        progress = 0
        
        DispatchQueue.global(qos: .userInitiated).async {
            let allBenchmarks = getAllBenchmarks()
            var newResults: [BenchmarkResult] = []
            
            for (index, bench) in allBenchmarks.enumerated() {
                DispatchQueue.main.async {
                    currentTest = bench.name
                    progress = Double(index) / Double(allBenchmarks.count)
                }
                
                let result = bench.run()
                newResults.append(result)
            }
            
            DispatchQueue.main.async {
                results = newResults
                isRunning = false
                progress = 1.0
                currentTest = ""
            }
        }
    }
}

struct ResultRow: View {
    let result: BenchmarkResult
    
    var body: some View {
        VStack(alignment: .leading, spacing: 4) {
            Text(result.name)
                .font(.subheadline.monospaced())
            HStack {
                Label(formatTime(result.boltffiTimeNs), systemImage: "hare.fill")
                    .foregroundColor(.green)
                    .font(.caption)
                Spacer()
                Label(formatTime(result.uniffiTimeNs), systemImage: "tortoise.fill")
                    .foregroundColor(.orange)
                    .font(.caption)
                Spacer()
                Text(String(format: "%.1fx", result.speedup))
                    .font(.caption.bold())
                    .foregroundColor(result.speedup >= 1 ? .green : .red)
            }
        }
        .padding(.vertical, 2)
    }
    
    func formatTime(_ ns: UInt64) -> String {
        if ns >= 1_000_000 {
            return String(format: "%.1f ms", Double(ns) / 1_000_000)
        } else if ns >= 1_000 {
            return String(format: "%.1f μs", Double(ns) / 1_000)
        } else {
            return "\(ns) ns"
        }
    }
}

struct Bench {
    let name: String
    let category: String
    let iterations: Int
    let boltffiBlock: () -> Void
    let uniffiBlock: () -> Void
    
    func run() -> BenchmarkResult {
        for _ in 0..<10 { boltffiBlock(); uniffiBlock() }
        
        let boltffiTime = measure(iterations: iterations, boltffiBlock)
        let uniffiTime = measure(iterations: iterations, uniffiBlock)
        
        return BenchmarkResult(
            name: name,
            category: category,
            boltffiTimeNs: boltffiTime,
            uniffiTimeNs: uniffiTime
        )
    }
    
    private func measure(iterations: Int, _ block: () -> Void) -> UInt64 {
        let start = CACurrentMediaTime()
        for _ in 0..<iterations {
            block()
        }
        let end = CACurrentMediaTime()
        let totalNs = UInt64((end - start) * 1_000_000_000)
        return totalNs / UInt64(iterations)
    }
}

